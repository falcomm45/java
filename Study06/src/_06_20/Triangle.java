package _06_20;

public class Triangle {
	
	private int bottom;
	private int height;
	
	public void setbottom(int bottom) {
		this.bottom = bottom;
	}
	
	public int getbottom() {
		return bottom;
	}
	
	public void setHeight(int height) {
		this.height = height;
	}
	
	public int getHeight() {
		return height;
	}
	 
}

